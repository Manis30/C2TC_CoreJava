package com.tnsif.dayone;

public class typeCastingDemo {

	public static void main(String[] args) {
		byte a=120;
		int b=a;
		System.out.println(b);
		short c=3654;
		long l=c;
		System.out.println(l);
		int e=65;
		char ch=(char)e;
		System.out.println(ch);
		float f=34.67f;
		int i=(int)f;
		System.out.println(i);
	}

}
